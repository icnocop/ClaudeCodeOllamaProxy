using Microsoft.UI.Xaml;
using Windows.Win32;
using Windows.Win32.Foundation;
using Windows.Win32.UI.WindowsAndMessaging;

namespace ClaudeCodeOllamaProxy.UI.Services;

/// <summary>
/// Brings the main window to the foreground. WinUI's <see cref="Window.Activate"/> alone does not
/// reliably steal focus when the window is behind other windows (or minimized), so this restores it
/// if minimized and calls Win32 <c>SetForegroundWindow</c>. To get past the OS foreground lock that
/// blocks a background process from stealing focus, it temporarily attaches this thread's input to
/// the current foreground thread (the standard AttachThreadInput trick). The Win32 entry points are
/// generated by CsWin32 from <c>NativeMethods.txt</c>.
/// </summary>
internal static class WindowActivationHelper
{
    public static void BringToFront(Window window)
    {
        var hwnd = new HWND(WinRT.Interop.WindowNative.GetWindowHandle(window));
        if (hwnd == default)
            return;

        if (PInvoke.IsIconic(hwnd))
            PInvoke.ShowWindow(hwnd, SHOW_WINDOW_CMD.SW_RESTORE);

        var foreground = PInvoke.GetForegroundWindow();
        var foregroundThread = PInvoke.GetWindowThreadProcessId(foreground, out _);
        var thisThread = PInvoke.GetCurrentThreadId();

        var attached = foregroundThread != thisThread
            && PInvoke.AttachThreadInput(thisThread, foregroundThread, true);
        try
        {
            // BringWindowToTop raises an already-visible window above the others; SetForegroundWindow
            // gives it focus. Both are needed: focus alone won't raise it, raising alone won't focus it.
            PInvoke.BringWindowToTop(hwnd);
            PInvoke.SetForegroundWindow(hwnd);
        }
        finally
        {
            if (attached)
                PInvoke.AttachThreadInput(thisThread, foregroundThread, false);
        }
    }
}
